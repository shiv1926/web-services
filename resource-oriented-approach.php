<p>A resource-oriented architecture (ROA) is the structural design supporting the internetworking of resources. A resource, in this context, is any entity that can be identified and assigned a uniform resource identifier (URI).</p>
<p>Resource-Oriented clients are client programs that consume services designed in accordance with the REST architectural principles. The key REST principles include:</p>
<ol>
    <li>The concept of resource (for example, a document is a resource)</li>
    <li>Every resource given a unique ID (for example, document URL)</li>
    <li>Resources can be related (for example, One document linking to another)</li>
    <li>Use of standard (HTTP, HTML, XML)</li>
    <li>Resources can have multiple forms (for example, status of a document, updated, validated, deleted)</li>
    <li>Communicate in a stateless fashion using HTTP (for example, subsequent requests not related to each other) comparision between soap and rest</li>
</ol>
